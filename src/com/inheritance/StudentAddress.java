package com.inheritance;

public class StudentAddress {
	String flatNumber;
	String houseNumber;
	String street;
	String state;
	String city;
	String country;
	String zipCode;
	public StudentAddress(String flatNumber, String houseNumber, String street, String state, String city,
			String country, String zipCode) {
		this.flatNumber = flatNumber;
		this.houseNumber = houseNumber;
		this.street = street;
		this.state = state;
		this.city = city;
		this.country = country;
		this.zipCode = zipCode;
	}
}
